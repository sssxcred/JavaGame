package model;

/**
 * The {@code ObjectType} enum represents different types of game objects.
 */
public enum ObjectType {
    /** Represents a key object. */
    KEY,
    /** Represents a blood shard object. */
    BLOOD_SHARD,
    /** Represents a chest object. */
    CHEST,
    /** Represents a non-playable character (NPC) object. */
    NPC
}
